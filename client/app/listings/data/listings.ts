export const formatPrice = (price: number) => {
  const formatted = price.toLocaleString("vi-VN", {
    minimumFractionDigits: Number.isInteger(price) ? 0 : 1,
    maximumFractionDigits: 1,
  });
  return `${formatted} triệu`;
};

